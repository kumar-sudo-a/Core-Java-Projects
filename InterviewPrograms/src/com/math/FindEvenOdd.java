package com.math;

public class FindEvenOdd  {  
	public static void main(String args[])	{  
		int number=89; 
		//returns true if num is perfectly divisible by 2  
		if(number % 2 == 0)  
			System.out.println(number +" is even number.");  
		else  
			System.out.println(number +" is odd number.");  
	}  
}  